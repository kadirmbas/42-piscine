int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	newbr;

	i = 0;
	sign = 1;
	newbr = 0;
	while ((str[i] <= 13 && str[i] >= 9) || str[i] == ' ')
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			str[i] *= -1;
			i++;
		}
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		newbr = newbr * 10 + (str[i] - '0');
		i++;
	}
	return (newbr * sign);
}
