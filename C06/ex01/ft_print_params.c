#include<unistd.h>

int	main(int argc, char **argv)
{
	int	y;
	int	b;

		y = 1;
		b = 0;
	if (argc > 1)
	{
		while (y < argc)
		{
			b = 0;
			while (argv[y][b] != '\0')
			{
				write(1, &argv[y][b], 1);
					b++;
			}
			write(1, "\n", 1);
				y++;
		}
	}
	return (0);
}
